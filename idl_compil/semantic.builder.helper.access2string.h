namespace gie_idl {

    inline
    const string_t map_access2string(const access_t& access)
    {
        if( access==access_public )
            return wxT("public");
        if( access==access_private )
            return wxT("private");
        if( access==access_protected )
            return wxT("protected");

        GIE_LIB_NOT_IMPLEMENTED;
    }
}
