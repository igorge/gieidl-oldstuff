namespace gie_idl {

    struct backends_manager_t
        : ::std::map<string_t, backend_meta_info_t const*> 
    {
        backends_manager_t();
        bool is_valid_backend_name(const string_t& name)const
        {
            return find(name) != end();
        }
        backend_meta_info_t const* get_back_end_meta_info(const string_t& name)const 
        {
            const const_iterator iter = find(name);
            if( iter==end() )
                return NULL;
            else
                return iter->second;
        }
        const backend::backend_interface_ptr_t create_back_end(const string_t& name);
    private:
    };
}
